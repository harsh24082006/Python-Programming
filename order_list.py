sports =['cricket', 'football', 'hockey', 'kabaddi', 'kho-kho']

print("6. Finding Max and Min (Alphabetical Order):")
print(f"List: {sports}")

# Alphabetically highest (closest to Z)
print(f"Maximum element: '{max(sports)}'")

# Alphabetically lowest (closest to A)
print(f"Minimum element: '{min(sports)}'")
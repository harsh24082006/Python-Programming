sports =['cricket', 'football', 'hockey', 'kabaddi', 'kho-kho']

print("3. Accessing Elements:")
print(f"Original list: {sports}\n")

# Positive Indexing
print(f"First sport (Index 0): {sports[0]}")
print(f"Third sport (Index 2): {sports[2]}")

# Negative Indexing
print(f"Last sport (Index -1): {sports[-1]}")
print(f"Second to last sport (Index -2): {sports[-2]}")